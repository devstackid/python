angka = int(input("Masukkan Bilangan : "))

# jika hasil baginya bernilai genap maka akan menampilkan
# jika angka dibagi 2 tidak ada sisanya maka bilangan tersebut adalah bilangan genap
# Jika angka dibagi 2 ada sisa hasil baginya maka biangan tersebut adalah bilangan ganjil

if(angka % 2 == 0):
    print ("Bilangan yang anda masukkan adalah bilangan genap")
else:
    print("Bilangan yang anda masukkan adalah bilangan ganji")